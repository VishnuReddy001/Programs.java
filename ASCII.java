class ASCII {
    public static void main(String[] args) {
        char char1 = 'a';
        char char2 = 'b';
        int ASCIIvalue1 = char1;
        int ASCIIvalue2 = char2;
        System.out.println("The ASCII value of " + char1 + "is" + ASCIIvalue1);
        System.out.println("The ASCII value of " + char2 + "is" + ASCIIvalue2);
    }
}